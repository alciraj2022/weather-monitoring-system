	  	  	  Lcd_clear(&lcd);
	  	  	  Lcd_cursor(&lcd, 0,0);
	  	  	  Lcd_string(&lcd, "Temperature  (C)"); //displays temp on LCD screen
	  	  	  Lcd_cursor(&lcd, 1,7);
	  	  	  Lcd_int(&lcd, temp_C);

	  	  	  HAL_Delay(5000);

	  	  	  key = 0; //changes key value back to zero

	  	  	  Lcd_cursor(&lcd, 0,0);
	  	  	  Lcd_string(&lcd, "Weather  Station");
	  	  	  Lcd_cursor(&lcd, 1,2);
